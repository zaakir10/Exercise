let a = 15;
let b = "15"

// Equal to
console.log(a == b);

// not equal to
console.log(a != b);

// strictly
console.log(a === b);

// strictly but not same type

console.log(a !== b);

// greater than

console.log(a < b);

// less than

console.log(a > b);

// greater than or equal

console.log(a <= b);

// less than or equal

console.log(a >= b);

